package ForLoop;

public class Pattern {

	public static void main(String[] args) {
		int n=5;
		for(int i=1;i<=3;i++)
		{
			
			for(int j=1;j<=3;j++)
			{
				System.out.print("* ");
			}
			System.out.println();
		
		for(int k=1;k<=3;k++)
		{
			System.out.print("x ");
		}
		System.out.println();
	}

}
}
